package com.kjjcsoft.bean;

public class EMIBean {
	public String principal;
	public String date;
	public String payablePrincipal;
	public String payableInterest;
	public String payableAmount;
	public String remainingPrincipal;
	public String getPrincipal() {
		return principal;
	}
	public void setPrincipal(String principal) {
		this.principal = principal;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPayablePrincipal() {
		return payablePrincipal;
	}
	public void setPayablePrincipal(String payablePrincipal) {
		this.payablePrincipal = payablePrincipal;
	}
	public String getPayableInterest() {
		return payableInterest;
	}
	public void setPayableInterest(String payableInterest) {
		this.payableInterest = payableInterest;
	}
	public String getPayableAmount() {
		return payableAmount;
	}
	public void setPayableAmount(String payableAmount) {
		this.payableAmount = payableAmount;
	}
	public String getRemainingPrincipal() {
		return remainingPrincipal;
	}
	public void setRemainingPrincipal(String remainingPrincipal) {
		this.remainingPrincipal = remainingPrincipal;
	}
	
}
